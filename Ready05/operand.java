public class operand {
  public static void main(String[] args) {
    int a = 15;
    int c;
    c = a + 15;
    System.out.println(c);
    c += 15;
    System.out.println(c);

    int y = 5;
    int d = y++;
    System.out.println(y);
    System.out.println(d);

    int z = 5;
    int e = ++z;
    System.out.println(z);
    System.out.println(e);

    boolean boolean1 = true;
    boolean boolean2 = false;
    boolean boolean3;
  }
}