public class Switch01 {
  public static void main(String[] args) {
    int val = 0;
    switch (val) {
      case 3:
        System.out.println("よくできました");
        break;
      case 2:
        System.out.println("まぁまぁですね");
        break;
      default:
        System.out.println("残念でした！");
    }
  }
}